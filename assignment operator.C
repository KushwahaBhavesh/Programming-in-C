// write a program using assignment operator .

#include<stdio.h>
#include<conio.h>

int main(){

    int x=2,z;

    z=x;
    printf("z=%d\n",z);

    z+=x;
    printf("z=%d\n",z);

    z-=x;
    printf("z=%d\n",z);

    z*=x;
    printf("z=%d\n",z);

    z/=x;
    printf("z=%d\n",z);

    z%=x;
    printf("z=%d\n",z);

}